            <h2>Convocatoria</h2><br>
            <p>Una iniciativa de Codeando Xalapa, en la que colaboran Open Street Map y el H. Ayuntamiento de Xalapa, así como el Instituto Tecnológico Superior de Xalapa y la Universidad Veracruzana; se convoca a la ciudadanía que radique en la ciudad de Xalapa y sus alrededores, con edad mínima de 18 años, a participar en el “Mapatón Xalapa 2016”.</p><br>

            <h3>Objetivo</h3><br>
            <p>Obtener la ubicación y realizar el trazado georreferenciado de las rutas del transporte público de autobuses concesionados de la ciudad de Xalapa, con la finalidad de generar información que sea de uso abierto para manipular, exportar y representarlas en mapas, infografías, aplicaciones, entre otras;  a través del uso de datos abiertos, plataformas open source y sin costo de uso.</p><br>

            <h3>Convoca a</h3><br>
            <p>Todas aquellas personas que quieran participar en el trazado de rutas del transporte público de autobuses concesionados de la ciudad de Xalapa siguiendo los requisitos generales.</p><br>

            <h3>¿Por qué debes formar parte de Mapatón Xalapa 2016?</h3><br>
            <p><span>1.</span> Colaborarás en la construcción de la <strong>base de datos</strong> abiertos del sistema de transporte público concesionado de autobuses de la ciudad de Xalapa.</p>
            <p><span>2.</span> Participarás en un trabajo colaborativo en el que se integran Organizaciones Civiles, ONG’s y Comunidades Universitarias.</p>
            <p><span>3.</span> Tienes la oportunidad de ganar diversos <strong>premios.</strong></p>
            <p><span>4.</span> Contribuirás a que todos poseamos <strong>información actualizada</strong> y abierta sobre nuestro sistema de transporte público concesionado.</p>
            <p><span>5.</span> Ayudarás a que operadores de transporte, estudiantes, investigadores, emprendedores y servidores públicos, entre muchos otros tengan información valiosa en <strong>beneficio de la ciudadanía.</strong></p><br>

            <h3>¿Qué debo hacer para participar?</h3>
            <h4>Requisitos generales</h4><br>
            <p><span>1.</span> Ser mayor de 18 años.</p>
            <p><span>2.</span> Tener un smartphone con Android, GPS y cámara (no es necesario contar con datos móviles).</p>
            <p><span>3.</span> Descargar la aplicación MapMap en Google Play o desde la web <a href="http://mapaton.org">www.mapaton.org</a> que estará disponible para teléfonos Android con versión 4.1 y superior.</p>
            <p><span>4.</span> Llenar el formulario de registro en la siguiente dirección: <a href="http://mapaton.org/registro" target="_blank">www.mapaton.org/registro</a></p>

