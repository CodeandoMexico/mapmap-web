            <h3><span>14.</span> ¿Usar la app MapMap es difícil?</h3>
            <p>De hecho es más fácil que jugar Candy Crush. Ya con la app MapMap instalada: 1. El equipo de Mapatón te asignará una ruta. 2. Tienes que asegurarte de tener listas tus cosas y preparar todo para iniciar el recorrido. 3. Una vez iniciando el recorrido, la aplicación irá trazando el camino que estás realizando. 4. Al terminar la ruta, le dirás a la app que ya ha terminado el recorrido y ésta enviará la ruta que realizaste a nuestra base de datos. Y voilà, eso es todo. Fácil, ¿no? </p>

