            <h3><span>01.</span> ¿QUÉ ES?</h3>
            <p>Mapa + Maratón = Mapatón. Se lee raro, ¿no?
            Un Mapatón es un evento coordinado que, con la participación de los ciudadanos, planea mapear las rutas de medios de transporte en una ciudad. Vamos a verlo de esta manera. Con solo un teléfono celular podrás digitalizar el recorrido que realizas mientras te transportas en un transporte urbano.</p>

            <p>Lo sabemos. Es algo nuevo, pero vas a ver que es super fácil entender de qué se trata, y los beneficios son muchísimos. Sigue leyendo para que conozcas más acerca de esto.</p>

            <h3><span>02.</span> ¿en qué consiste?</h3>
            <p>Un Mapatón consiste en trazar las rutas que realizas en algún medio de transporte para tener una base de datos de todos los recorridos, y así poder crear soluciones y estrategias que puedan mejorar el tráfico de una ciudad. En pocas palabras es obtener información de la ruta que un vehículo realiza cuando se traslada de un punto A a un punto B.</p>
